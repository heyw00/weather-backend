export class AppController {
  constructor(private readonly appService: AppService) {}

  @UseGuards(AuthGuard('jwt'))
  @Get()
  get(@Req() req: Request) {
    console.log(req.user.userId);
    return 'JWT 인증 성공';
  }
}
